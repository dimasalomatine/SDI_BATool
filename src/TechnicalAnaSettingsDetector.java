    private void toglegroup(int group,boolean state)
     {
     	switch(group)
     	{
     		case 1:
     				rsicb1s.setEnabled(state);
    	 			rsi1minval.setEnabled(state);
    	 			rsi1maxval.setEnabled(state);	
                                enableoprsibetween12();
     		break;
     		case 2:
     				rsicb2s.setEnabled(state);
    	 			rsi2minval.setEnabled(state);
    	 			rsi2maxval.setEnabled(state);	
                                enableoprsibetween12();
     		break;
                
     		case 3:
     				cdchsup.setEnabled(state);
    	 			closechpup.setEnabled(state);	
     		break;
                case 31:
     				cdchsdown.setEnabled(state);
    	 			closechpdown.setEnabled(state);	
     		break;
     		case 4:
     				volchsup.setEnabled(state);
    	 			volumechpup.setEnabled(state);	
     		break;
                case 41:
     				volchsdown.setEnabled(state);
    	 			volumechpdown.setEnabled(state);	
     		break;
                case 5:
     				pls_min.setEnabled(state);
    	 			pls_max.setEnabled(state);	
     		break;
     	}
     }
